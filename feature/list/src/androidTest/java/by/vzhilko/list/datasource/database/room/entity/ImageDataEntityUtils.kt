fun areImageDataEntityListsEqual(
    expected: List<ImageDataEntity>,
    actual: List<ImageDataEntity>
): Boolean {
    return expected.isEqual(
        list = actual,
        equalBlock = { first, second ->
            first.imageDataId == second.imageDataId
                    && first.tags == second.tags
                    && first.previewUrl == second.previewUrl
                    && first.largeImageUrl == second.largeImageUrl
                    && first.downloads == second.downloads
                    && first.likes == second.likes
                    && first.comments == second.comments
                    && first.user == second.user
        }
    )
}